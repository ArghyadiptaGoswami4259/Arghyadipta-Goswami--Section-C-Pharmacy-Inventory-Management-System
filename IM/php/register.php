<?php
include('../config/db.php');

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $name = $_POST['name'];
    $email = $_POST['email'];
    $password = $_POST['password'];
    $confirm_password = $_POST['confirm_password'];

    if ($password !== $confirm_password) {
        echo "<div class='error'>Passwords do not match. Please try again.</div>";
    } else {
        $hashed_password = password_hash($password, PASSWORD_BCRYPT);

        // Check if email already exists
        $query = "SELECT * FROM users WHERE email = ?";
        $stmt = $conn->prepare($query);
        $stmt->bind_param("s", $email);
        $stmt->execute();
        $result = $stmt->get_result();

        if ($result->num_rows > 0) {
            echo "<div class='error'>This email is already registered. Please use a different email.</div>";
        } else {
            // Insert new user
            $insertQuery = "INSERT INTO users (name, email, password) VALUES (?, ?, ?)";
            $stmt = $conn->prepare($insertQuery);
            $stmt->bind_param("sss", $name, $email, $hashed_password);

            if ($stmt->execute()) {
                echo "<div class='success'>Registration successful! <a href='login.php'>Login here</a></div>";
            } else {
                echo "<div class='error'>Error: Could not register user.</div>";
            }
        }
    }
}
